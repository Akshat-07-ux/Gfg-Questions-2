class Solution {
  public:
    int maxSubarrayXOR(vector<int>& arr, int k) {
        int n = arr.size();
        
        // XOR of first window
        int currXor = 0;
        for (int i = 0; i < k; i++) {
            currXor ^= arr[i];
        }
        
        int maxXor = currXor;
        
        // Sliding window
        for (int i = k; i < n; i++) {
            currXor ^= arr[i - k];  // remove left element
            currXor ^= arr[i];      // add right element
            maxXor = max(maxXor, currXor);
        }
        
        return maxXor;
    }
};
